// Logical Operators:

// 1. ' && ' And (both sides must be true for the expression to be true);

let num_1 : number = 55;
let num_2 : number = 55;
let num_3 : number = 8;
let num_4 : number = 5;
console.log(num_1 === num_2 && num_3 === num_4);  // Output false;



// 2. ' || ' or (at least one side must be true for the expression to be true);
let num$1 : number = 55;
let num$2 : number = 55;
let num$3 : number = 8;
let num$4 : number = 5;
console.log(num$1 === num$2 || num$3 === num$4);   // Output true;



// 3. ' ! ' Not (inverts the truth value);
let numbe1 : number = 40;
let numbe2 : number = 10;
console.log(numbe1 !== numbe2);     // Output true;